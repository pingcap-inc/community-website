import React from 'react';
import NextHead from 'next/head';

const Head = ({ author, title, titleSuffix, description, keyword, googleAnalyticsId }) => {
  const fullTitle = `${title}${titleSuffix}`;
  const keywordStr = Array.isArray(keyword) ? keyword.join(',') : keyword;

  return (
    <NextHead>
      <title>{fullTitle}</title>
      <meta name="description" content={description} />
      <meta name="author" content={author} />
      {keywordStr && <meta name="keyword" content={keywordStr} />}

      {/* 'Open Graph protocol' enables rich sharing info on Facebook */}
      <meta name="og:title" content={fullTitle} />
      <meta name="og:description" content={description} />
      <meta name="og:type" content="website" />
      <meta name="og:image" content="/images/icon.svg" />
      <meta name="og:image:width" content="200" />
      <meta name="og:image:height" content="200" />

      {/* for sharing to twitter */}
      <meta name="twitter:title" content={fullTitle} />
      <meta name="twitter:description" content={description} />
      <meta name="twitter:card" content="summary_large_image" />
      <meta name="twitter:creator" content={author} />

      {/* favicons should be generated by https://realfavicongenerator.net/ */}
      <link rel="apple-touch-icon" sizes="180x180" href="/favicons/community/apple-touch-icon.png" />
      <link rel="icon" type="image/png" sizes="32x32" href="/favicons/community/favicon-32x32.png" />
      <link rel="icon" type="image/png" sizes="16x16" href="/favicons/community/favicon-16x16.png" />
      <link rel="manifest" href="/favicons/community/site.webmanifest" />
      <link rel="shortcut icon" href="/favicons/community/favicon.ico" />
      <meta name="theme-color" content="#ffffff" />

      {/* GA setup: https://stackoverflow.com/a/62552263/14257627 */}
      <script async src={`https://www.googletagmanager.com/gtag/js?id=${googleAnalyticsId}`}></script>
      <script
        dangerouslySetInnerHTML={{
          __html: `
            window.dataLayer = window.dataLayer || [];
            function gtag(){dataLayer.push(arguments);}
            gtag('js', new Date());
            gtag('config', '${googleAnalyticsId}');
        `,
        }}
      />
    </NextHead>
  );
};

export default Head;
